const mongoose = require("mongoose");

const pendingUserSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
    trim: true
  },
  password: {
    type: String,
    required: true
  },
  role: {
    type: String,
    enum: ["student", "startup", "admin"],
    required: true
  },
  otp: {
    type: String,
    required: true
  },
  expiresAt: {
    type: Date,
    required: true,
    index: { expires: 0 } // Document expires at the time specified in expiresAt
  }
}, { timestamps: true });

module.exports = mongoose.model("PendingUser", pendingUserSchema);
